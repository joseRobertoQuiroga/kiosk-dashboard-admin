import { useState } from 'react';
import { bulkCreateProductos } from '../../../service/api';

/**
 * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
 * üåê HOOK: useBulkApi
 * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
 * 
 * Hook para importar productos desde APIs externas
 * 
 * FUNCIONALIDADES:
 * - Probar conexi√≥n a API externa
 * - Extraer datos de forma din√°mica usando JSON path
 * - Validar estructura de datos
 * - Importar productos al sistema
 * 
 * CONFIGURACI√ìN:
 * - url: URL de la API externa
 * - token: Token de autenticaci√≥n (opcional)
 * - dataPath: Ruta JSON para extraer datos (ej: "data.items")
 * 
 * ESTRUCTURA ESPERADA DEL JSON:
 * [
 *   {
 *     "codigo": "123",
 *     "nombre": "Producto",
 *     "categoria": "Bebidas",
 *     "precio": 10.50,
 *     "detalles": "Descripci√≥n",
 *     "promocion": "Oferta" // OPCIONAL
 *   }
 * ]
 * 
 * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
 */

interface ApiConfig {
  url: string;
  token?: string;
  dataPath?: string;
}

interface ProductoApi {
  codigo: string;
  nombre: string;
  categoria: string;
  precio: number;
  detalles: string;
  promocion?: string;
}

export const useBulkApi = () => {
  const [loading, setLoading] = useState(false);
  const [preview, setPreview] = useState<ProductoApi[]>([]);
  const [error, setError] = useState<string | null>(null);
  const [fullData, setFullData] = useState<ProductoApi[]>([]);

  /**
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * üîå PROBAR CONEXI√ìN A API EXTERNA
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * Conecta a la API, extrae datos y valida estructura
   */
  const testConnection = async (config: ApiConfig) => {
    setLoading(true);
    setError(null);
    setPreview([]);
    setFullData([]);

    console.group('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üîå [BulkAPI] Probando conexi√≥n');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('URL:', config.url);
    console.log('Token:', config.token ? '‚úì Presente' : '‚úó No configurado');
    console.log('DataPath:', config.dataPath || 'Ra√≠z del JSON');

    try {
      // üî• REALIZAR REQUEST
      const headers: any = {};
      if (config.token) {
        headers.Authorization = `Bearer ${config.token}`;
      }

      const response = await fetch(config.url, { headers });

      if (!response.ok) {
        throw new Error(`HTTP ${response.status}: ${response.statusText}`);
      }

      const json = await response.json();
      console.log('üì¶ [BulkAPI] Respuesta recibida');

      // üî• EXTRAER DATOS USANDO PATH
      const extracted = extractByPath(json, config.dataPath);
      
      if (!extracted || extracted.length === 0) {
        throw new Error('No se encontraron datos en la ruta especificada');
      }

      console.log('‚úÖ [BulkAPI] Datos extra√≠dos:', extracted.length);

      // üî• VALIDAR ESTRUCTURA
      const validated = validateApiData(extracted);
      
      if (validated.length === 0) {
        throw new Error('Los datos no tienen la estructura correcta');
      }

      // üî• GUARDAR DATOS COMPLETOS Y PREVIEW
      setFullData(validated);
      setPreview(validated.slice(0, 5)); // Primeros 5 para preview
      
      console.log('‚úÖ [BulkAPI] Conexi√≥n exitosa');
      console.log('Total productos v√°lidos:', validated.length);
      console.groupEnd();

      return validated;

    } catch (e: any) {
      console.error('‚ùå [BulkAPI] Error de conexi√≥n:', e);
      console.groupEnd();
      
      const errorMsg = e.message || 'No se pudo conectar a la API';
      setError(errorMsg);
      throw e;

    } finally {
      setLoading(false);
    }
  };

  /**
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * üì¶ IMPORTAR PRODUCTOS DESDE API
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   */
  const importFromApi = async (data?: ProductoApi[]) => {
    const dataToImport = data || fullData;

    if (!dataToImport || dataToImport.length === 0) {
      alert('‚ö†Ô∏è No hay datos para importar. Primero prueba la conexi√≥n.');
      return;
    }

    setLoading(true);
    console.group('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('üöÄ [BulkAPI] Importando productos desde API');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('Total a importar:', dataToImport.length);

    try {
      const result = await bulkCreateProductos(dataToImport);

      console.log('‚úÖ [BulkAPI] Importaci√≥n completa:', result);
      console.groupEnd();

      // üî• MOSTRAR RESULTADO
      if (result.errores > 0) {
        let mensaje = `‚úÖ ${result.insertados} productos importados\n`;
        mensaje += `‚ùå ${result.errores} productos con errores\n\n`;
        
        if (result.detalles && result.detalles.length > 0) {
          mensaje += 'Detalles de errores:\n';
          result.detalles.slice(0, 10).forEach(detalle => {
            mensaje += `  ‚Ä¢ Fila ${detalle.fila}: ${detalle.error}\n`;
          });
          
          if (result.detalles.length > 10) {
            mensaje += `\n... y ${result.detalles.length - 10} errores m√°s`;
          }
        }
        
        alert(mensaje);
      } else {
        alert(`‚úÖ ¬°√âxito! ${result.insertados} productos importados desde la API`);
      }

      // üî• LIMPIAR ESTADO
      setPreview([]);
      setFullData([]);

    } catch (e: any) {
      console.error('‚ùå [BulkAPI] Error importando:', e);
      console.groupEnd();
      
      const errorMsg = e.response?.data?.message || e.message || 'Error desconocido';
      alert(`‚ùå Error al importar productos:\n${errorMsg}`);

    } finally {
      setLoading(false);
    }
  };

  /**
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * üîç EXTRAER DATOS DIN√ÅMICAMENTE POR PATH
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * Permite navegar en JSON anidado usando notaci√≥n punto
   * Ejemplo: "data.items" accede a obj.data.items
   */
  const extractByPath = (obj: any, path?: string): any[] => {
    if (!path || path.trim() === '') {
      // Sin path: asumir que obj es el array directamente
      return Array.isArray(obj) ? obj : [];
    }

    // Navegar por el path
    const keys = path.split('.');
    let current = obj;

    for (const key of keys) {
      if (current && typeof current === 'object' && key in current) {
        current = current[key];
      } else {
        console.warn(`‚ö†Ô∏è [BulkAPI] No se encontr√≥ la ruta: ${path}`);
        return [];
      }
    }

    return Array.isArray(current) ? current : [];
  };

  /**
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * ‚úÖ VALIDAR Y NORMALIZAR DATOS DE API
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   */
  const validateApiData = (data: any[]): ProductoApi[] => {
    const validados: ProductoApi[] = [];

    console.group('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
    console.log('‚úÖ [BulkAPI] Validando estructura de datos');
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');

    data.forEach((item, index) => {
      try {
        // üî• VALIDAR CAMPOS OBLIGATORIOS
        if (!item.codigo || !item.nombre || !item.categoria || !item.detalles) {
          console.warn(`‚ö†Ô∏è [${index}] Campos obligatorios faltantes`);
          return;
        }

        // üî• VALIDAR PRECIO
        const precio = Number(item.precio);
        if (isNaN(precio) || precio <= 0) {
          console.warn(`‚ö†Ô∏è [${index}] Precio inv√°lido: ${item.precio}`);
          return;
        }

        // üî• NORMALIZAR PRODUCTO
        validados.push({
          codigo: String(item.codigo).trim(),
          nombre: String(item.nombre).trim(),
          categoria: String(item.categoria).trim(),
          precio: precio,
          detalles: String(item.detalles).trim(),
          promocion: item.promocion ? String(item.promocion).trim() : '' // üî• OPCIONAL
        });

      } catch (error) {
        console.warn(`‚ö†Ô∏è [${index}] Error procesando item:`, error);
      }
    });

    console.log('Productos v√°lidos:', validados.length);
    console.log('Productos descartados:', data.length - validados.length);
    console.groupEnd();

    return validados;
  };

  /**
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   * üîÑ LIMPIAR ESTADO
   * ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ
   */
  const reset = () => {
    setPreview([]);
    setFullData([]);
    setError(null);
    console.log('üîÑ [BulkAPI] Estado limpiado');
  };

  return {
    loading,          // Estado de carga
    preview,          // Preview de primeros 5 productos
    error,            // Mensaje de error
    fullData,         // Datos completos extra√≠dos
    testConnection,   // Funci√≥n para probar API
    importFromApi,    // Funci√≥n para importar datos
    reset,            // Funci√≥n para limpiar estado
  };
};